# Declare and assign values to two integer variables
int1 = 10
int2 = 20

# Declare and assign values to two floating-point variables
float1 = 1.5
float2 = 2.5

# Declare and assign values to two string variables
string1 = "Hello"
string2 = "World"

# Add the two integers and assign the result to a new variable
result = int1 + int2
print("int1 + int2:", result)

# Subtract the two integers and assign the result to a new variable
result = int1 - int2
print("int1 - int2:", result)

# Multiply the two integers and assign the result to a new variable
result = int1 * int2
print("int1 * int2:", result)

# Divide the two integers and assign the result to a new variable
result = int1 / int2
print("int1 / int2:", result)

# Add the two floating-point numbers and assign the result to a new variable
result = float1 + float2
print("float1 + float2:", result)

# Subtract the two floating-point numbers and assign the result to a new variable
result = float1 - float2
print("float1 - float2:", result)

# Multiply the two floating-point numbers and assign the result to a new variable
result = float1 * float2
print("float1 * float2:", result)

# Divide the two floating-point numbers and assign the result to a new variable
